function phrasePass(s, rules) {
  let o = s;
  for (const [re, rep] of rules) o = o.replace(re, rep);
  return o;
}

function translate(en, locale) {
  if (EXACT[en]) return EXACT[en][locale === 'ja' ? 0 : 1];
  const words = locale === 'ja' ? WORDS_JA : WORDS_ZH_FULL;
  if (words[en]) return words[en];
  const rules = locale === 'ja' ? PHRASE_JA : PHRASE_ZH;
  let s = phrasePass(en, rules);
  if (words[s]) return words[s];
  return s;
}

function escStr(s) {
  return JSON.stringify(s);
}

function main() {
  const existing = loadExistingJaKeys();
  const taskStrings = collectTaskFieldStrings();
  const missing = [...taskStrings].filter((k) => k && !existing.has(k)).sort();

  const ja = {};
  const zh = {};
  for (const k of missing) {
    ja[k] = translate(k, 'ja');
    zh[k] = translate(k, 'zh-CN');
  }

  const lines = [
    '/**',
    ' * Auto-generated field-label exact translations for agent tasks (ui/features/agent/tasks).',
    ' * Regenerate: node tools/emit-task-field-translations.mjs',
    ' */',
    'export const TASK_FIELD_EXACT = {',
    '  ja: {',
  ];
  for (const k of missing) {
    lines.push(`    ${escStr(k)}: ${escStr(ja[k])},`);
  }
  lines.push('  },', "  'zh-CN': {");
  for (const k of missing) {
    lines.push(`    ${escStr(k)}: ${escStr(zh[k])},`);
  }
  lines.push('  },', '};', '');

  fs.writeFileSync(outPath, lines.join('\n'), 'utf8');
  console.log('Wrote', missing.length, 'keys to', path.relative(root, outPath));
}

main();
