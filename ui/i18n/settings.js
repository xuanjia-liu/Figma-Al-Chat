function getNestedValue(locale, key) {
  return translations[locale]?.[key];
}

export function normalizeSettingsLocale(locale) {
  if (locale === 'zh' || locale === 'zh-CN' || locale === 'cn') return 'zh-CN';
  if (locale === 'ja') return 'ja';
  return DEFAULT_SETTINGS_LOCALE;
}

export function getSettingsTranslation(locale, key, vars = {}) {
  const normalizedLocale = normalizeSettingsLocale(locale);
  let template = getNestedValue(normalizedLocale, key);
  if (template == null) {
    template = getNestedValue(DEFAULT_SETTINGS_LOCALE, key) ?? key;
  }

  return String(template).replace(/\{(\w+)\}/g, (_, token) => {
    const value = vars[token];
    return value == null ? `{${token}}` : String(value);
  });
}
